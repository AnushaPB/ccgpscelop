# create paths
BASE_PATH="../../data"
PREFIX="58-Sceloporus"
GENOME="$BASE_PATH/genome/ncbi_dataset/data/GCA_023333645.1/GCA_023333645.1_rSceOcc1.0.p_genomic.fna"
GENOME_NAME="GCA_023333645.1_rSceOcc1.0.p_genomic"
CALLABLE="$BASE_PATH/raw_data/58-Sceloporus_callable_sites.bed"

# 1. copy refer
cp $GENOME .
cp $GENOME.fai .

# 2. run k-mer split (takes a bit, generates a bunch of files starting with x)
seqbility-20091110/splitfa $GENOME_NAME.fna 100 | split -l 20000000

# 3. index genome
# do this in the same directory or for the alignment step it won't be able to find the index
bwa index $GENOME_NAME.fna

# 4. align with reference
# Concatenate all files starting with "xa" and create a single FASTQ file 
# done to combine multiple sequencing read files into one file.
cat xa* > reads.fq
# Align the sequencing reads in "test_reads.fq" to the reference genome "GCA_022605955.1_bRalLim1.0.p_genomic.fna" 
# (indexed before, the prefix of the index files are GCA_022605955.1_bRalLim1.0.p_genomic.fna)
# -R 1000000 sets the number of alignments to output, -O 3 and -E 3 set the gap open and extension penalties.
# The output is saved in the Sequence Alignment/Map (SAM) Index format as "test_reads.sai".
bwa aln -R 1000000 -O 3 -E 3 $GENOME_NAME.fna reads.fq > reads.sai
# Convert the alignment index file "test_reads.sai" back to the SAM format
# The SAM file contains detailed alignment information
# This step uses the same reference genome for the alignment.
bwa samse -f reads.sam $GENOME_NAME.fna reads.sai reads.fq

# 5. Generate a raw mask file from the alignment data
cat reads.sam | seqbility-20091110/gen_raw_mask.pl  > rawMask_100.fa

# 6. Generate the final mask file using "gen_mask".
seqbility-20091110/gen_mask -l 100 -r 0.5 rawMask_100.fa > mask_100_50.fa

