# Set paths
VCF_FILE="${BASE_PATH}/processed_data/58-Sceloporus_maf05_minDP5_maxDP50_rmsamp60_mm80_rmsamp40.vcf.gz"
CALLABLE="$BASE_PATH/raw_data/58-Sceloporus_callable_sites.bed"

# view to get individual IDs 
bcftools view -h $VCF_FILE
bcftools index -s $VCF_FILE

# get individuals and contigs
individuals=("Scelocci_MNW16-047-S58" "Scelocci_MLY21" "Scelocci_MLY75" "Scelocci_CAS241780" "Scelocci_CAS262282" "Scelocci_IW3258")
contig_string=$(bcftools view -h $VCF_FILE | grep '##contig' | cut -d ',' -f 1 | cut -d '=' -f 3 | head -n 10)

# Convert the contig string to an array
readarray -t contigs <<<"$contig_string"

for individual in "${individuals[@]}"
do
  for contig in "${contigs[@]}"
  do
    # picked a individual and scaffold
    bcftools view -s ${individual} -r ${contig} ${VCF_FILE} -Oz -o msmc.vcf.gz
    
    # removing any missing data
    bcftools view -i 'F_MISSING=0' msmc.vcf.gz -Oz -o msmc_nomiss.vcf.gz
    
    # generating inputs to MSMC
    msmc-tools/generate_multihetsep.py --mask=$CALLABLE \
                                       msmc_nomiss.vcf.gz > input_${individual}_${contig}.multihetsep.txt
    # running MSMC
    msmc2/build/release/msmc2  -t 2 -o test_${individual}_${contig}.msmc2 input_${individual}_${contig}.multihetsep.txt -p 1*4+25*2+1*4+1*6

    # remove intermediates
    rm msmc.vcf.gz
    rm msmc_nomiss.vcf.gz
  done
done

